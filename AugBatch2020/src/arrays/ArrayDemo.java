package arrays;

public class ArrayDemo {

	public static void main(String[] args) {
		
		int array1[] = new int[5];
		
		array1[0] = 100;
		array1[1] = 200;
		array1[2] = 300;
		array1[3] = 400;
		array1[4] = 500;
		
		System.out.println("The array elements are: ");
		System.out.println("1st element: "+array1[0]);
		System.out.println("2nd element: "+array1[1]);
		System.out.println("3rd element: "+array1[2]);
		System.out.println("4th element: "+array1[3]);
		System.out.println("5th element: "+array1[4]);
	}

}
